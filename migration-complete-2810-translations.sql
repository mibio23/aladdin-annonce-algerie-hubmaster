-- Vérification 1 : Total catégories
SELECT 
  'Total Categories' as check_type, 
  COUNT(*) as count,
  CASE WHEN COUNT(*) = 562 THEN '✓ OK' ELSE '✗ ERREUR' END as status
FROM categories;

-- Vérification 2 : Traductions par langue
SELECT 
  language_code,
  COUNT(*) as count,
  CASE WHEN COUNT(*) = 562 THEN '✓ OK' ELSE '✗ ERREUR' END as status
FROM category_translations
GROUP BY language_code
ORDER BY language_code;

-- Vérification 3 : Total traductions
SELECT 
  'Total Translations' as check_type,
  COUNT(*) as count,
  CASE WHEN COUNT(*) = 1124 THEN '✓ OK (562 × 2 langues: FR, AR)' ELSE '✗ ERREUR' END as status
FROM category_translations;

-- Vérification 4 : Catégories sans traduction
SELECT 
  'Categories sans traduction' as check_type,
  COUNT(DISTINCT c.id) as count,
  CASE WHEN COUNT(DISTINCT c.id) = 0 THEN '✓ OK' ELSE '✗ ERREUR' END as status
FROM categories c
LEFT JOIN category_translations ct ON c.id = ct.category_id
WHERE ct.id IS NULL;

-- Vérification 5 : Hiérarchie valide
SELECT 
  'Orphan Categories' as check_type,
  COUNT(*) as count,
  CASE WHEN COUNT(*) = 0 THEN '✓ OK' ELSE '✗ ERREUR' END as status
FROM categories
WHERE parent_id IS NOT NULL
AND parent_id NOT IN (SELECT id FROM categories);

-- Vérification 6 : Icônes manquantes (main categories only)
SELECT 
  'Missing Icons (Main Categories)' as check_type,
  COUNT(*) as count,
  CASE WHEN COUNT(*) = 0 THEN '✓ OK' ELSE '✗ WARNING' END as status
FROM categories
WHERE parent_id IS NULL
AND (icon IS NULL OR icon = '');

-- Vérification 7 : Slugs dupliqués
SELECT 
  'Duplicate Slugs' as check_type,
  COUNT(*) as count,
  CASE WHEN COUNT(*) = 0 THEN '✓ OK' ELSE '✗ ERREUR' END as status
FROM (
  SELECT slug, COUNT(*) as count
  FROM categories
  GROUP BY slug
  HAVING COUNT(*) > 1
) duplicates;

-- Vérification 8 : Distribution par niveau
SELECT 
  CASE 
    WHEN parent_id IS NULL THEN 'Niveau 0 (Main)'
    WHEN parent_id IN (SELECT id FROM categories WHERE parent_id IS NULL) THEN 'Niveau 1'
    ELSE 'Niveau 2+'
  END as level,
  COUNT(*) as count
FROM categories
GROUP BY level
ORDER BY level;

