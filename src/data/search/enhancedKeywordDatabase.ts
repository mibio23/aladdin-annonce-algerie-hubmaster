export const keywordCategories = Object.keys(enhancedKeywordDatabase);

// Fonction pour obtenir tous les mots-clés d'une catégorie
export function getKeywordsByCategory(category: string): string[] {
  return (enhancedKeywordDatabase as Record<string, any>)[category] || [];
}

// Fonction pour rechercher dans toutes les catégories
export function searchInKeywords(query: string): { category: string; matches: string[] }[] {
  const results: { category: string; matches: string[] }[] = [];
  const queryLower = query.toLowerCase();

  Object.entries(enhancedKeywordDatabase).forEach(([category, keywords]) => {
    if (Array.isArray(keywords)) {
      const matches = keywords.filter(keyword => 
        keyword.toLowerCase().includes(queryLower)
      );
      
      if (matches.length > 0) {
        results.push({ category, matches });
      }
    }
  });

  return results;
}

// Fonction pour obtenir des synonymes
export function getSynonyms(word: string): string[] {
  const wordLower = word.toLowerCase();
  const synonyms = enhancedKeywordDatabase.synonymes as Record<string, string[]>;
  
  // Chercher le mot comme clé
  if (synonyms[wordLower]) {
    return synonyms[wordLower];
  }
  
  // Chercher le mot dans les valeurs
  for (const [key, values] of Object.entries(synonyms)) {
    if (values.includes(wordLower)) {
      return [key, ...values.filter(v => v !== wordLower)];
    }
  }
  
  return [];
}

// Fonction pour obtenir des mots-clés saisonniers
export function getSeasonalKeywords(season?: string): string[] {
  const currentMonth = new Date().getMonth();
  let currentSeason = season;
  
  if (!currentSeason) {
    if (currentMonth >= 5 && currentMonth <= 8) currentSeason = 'été';
    else if (currentMonth >= 8 && currentMonth <= 10) currentSeason = 'rentrée';
    else if (currentMonth >= 11 || currentMonth <= 2) currentSeason = 'hiver';
    else currentSeason = 'été'; // printemps par défaut
  }
  
  return (enhancedKeywordDatabase.saisonnier as Record<string, string[]>)[currentSeason] || [];
}